
/**
 * Animal is an interface for any animal.
 *
 * @author Akul Arora
 * @version 1.0
 */
public interface Animal
{
    /**
     * Gets the sound of an animal
     * @return String sound
     */
    public String getSound();
    /**
     * Gets the type of animal
     * @return String type
     */
    public String getType();
}
